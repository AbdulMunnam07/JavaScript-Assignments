let num = prompt("Enter a day of the week")
switch (num) {
    case 1:
        console.log(`Friday`);
        break;
    case 2:
        console.log(`Saturday`);
        break;
    case 3:
        console.log(`Sunday`);
        break;
    case 4:
        console.log(`Monday`);
        break;
    case 5:
        console.log(`Tuesday`);
        break;
    case 6:
        console.log(`Wednesday`);
        break;
    case 7:
        console.log(`Thursday`);
        break;
    default:
        console.log(`Invalid input`);
        break;
}